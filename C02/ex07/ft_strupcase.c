char	*ft_strupcase(char	*str)
{
	char	*s_ptr;

	s_ptr = str;
	while (*s_ptr)
	{
		if ('a' <= *s_ptr && *s_ptr <= 'z')
			*s_ptr -= 32;
		s_ptr++;
	}
	return (str);
}
